// Задача 38: Задайте массив вещественных чисел.
// Найдите разницу между максимальным и минимальным элементов массива.



namespace ConsoleApp
{
    internal static class Program
    {
        private static void Main()
        {
            double[] arr = { 1.2, 3.4, 5.6, 7.8, 9.0 }; // Создаем массив вещественных чисел

            double min = arr[0]; // Инициализируем переменную для хранения минимального элемента
            double max = arr[0]; // Инициализируем переменную для хранения максимального элемента

            // Проходим по всем элементам массива и находим минимальный и максимальный элементы
            foreach (double x in arr)
            {
                if (x < min)
                {
                    min = x;
                }

                if (x > max)
                {
                    max = x;
                }
            }

            double diff = max - min; // Вычисляем разницу между максимальным и минимальным элементами

            Console.WriteLine("Массив: " + string.Join(", ", arr)); // Выводим массив на экран
            Console.WriteLine("Разница между максимальным и минимальным элементами: " + diff); // Выводим разницу на экран
        }
    }
}
